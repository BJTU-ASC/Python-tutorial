numbers = [0, 1, 2, 3, 4, 5, 6, 7]
my_numbers = numbers[2:6]
print(my_numbers)


numbers = [0, 1, 2, 3, 4, 5, 6, 7]
my_numbers = numbers[2:]
print(my_numbers)


numbers = [0, 1, 2, 3, 4, 5, 6, 7]
my_numbers = numbers[:6]
print(my_numbers)


numbers = [0, 1, 2, 3, 4, 5, 6, 7]
my_numbers = numbers[:]
print(my_numbers)


numbers = [0, 1, 2, 3, 4, 5, 6, 7]
my_numbers = numbers[1::2]
print(my_numbers)


numbers = [0, 1, 2, 3, 4, 5, 6, 7]
my_numbers = numbers[::-1]
print(my_numbers)


